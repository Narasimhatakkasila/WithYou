//register callback
const registerController = async (req, res) => {
  try {
    // Check if the user with the same email already exists
    const exisitingUser = await userModel.findOne({ email: req.body.email });
    if (exisitingUser) {
      return res
        .status(200)
        .send({ message: "User with this email already exists", success: false });
    }

    // Password length validation
    const password = req.body.password;
    if (password.length < 8) {
      return res.status(400).send({
        success: false,
        message: "Password must be at least 8 characters long",
      });
    }

    // Hash the password before saving
    const salt = await bcrypt.genSalt(10);
    const hashedPassword = await bcrypt.hash(password, salt);
    req.body.password = hashedPassword;

    // Save the new user
    const newUser = new userModel(req.body);
    await newUser.save();
    res.status(201).send({ message: "Registered Successfully", success: true });
  } catch (error) {
    // Handle unique constraint errors
    if (error.code === 11000) {
      const field = Object.keys(error.keyValue); // Get the field that caused the duplicate error
      return res.status(400).send({
        success: false,
        message: `${field[0]} already exists. Please use another ${field[0]}.`,
      });
    }

    // Handle other errors
    console.log(error);
    res.status(500).send({
      success: false,
      message: `Register Controller ${error.message}`,
    });
  }
};



// login callback
const loginController = async (req, res) => {
  try {
    const user = await userModel.findOne({ email: req.body.email });
    if (!user) {
      return res
        .status(200)
        .send({ message: "user not found", success: false });
    }
    const isMatch = await bcrypt.compare(req.body.password, user.password);
    if (!isMatch) {
      return res
        .status(200)
        .send({ message: "Invlid EMail or Password", success: false });
    }
    const token = jwt.sign({ id: user._id }, process.env.JWT_SECRET, {
      expiresIn: "1d",
    });
    res.status(200).send({ message: "Login Success", success: true, token });
  } catch (error) {
    console.log(error);
    res.status(500).send({ message: `Error in Login CTRL ${error.message}` });
  }
};

//forgotPasswordController

const forgotPasswordController = async (req, res) => {
  try {
    const { email, answer, newPassword } = req.body;

    // Input validation
    if (!email) {
      return res.status(400).send({ success: false, message: "Email is required" });
    }
    if (!answer) {
      return res.status(400).send({ success: false, message: "Answer is required" });
    }
    if (!newPassword) {
      return res.status(400).send({ success: false, message: "New Password is required" });
    }

    // Check if user exists with the provided email and answer
    const user = await userModel.findOne({ email, answer });
    if (!user) {
      return res.status(404).send({ success: false, message: "Wrong Email Or Answer" });
    }

    // Validate password length
    if (newPassword.length < 8) {
      return res.status(400).send({
        success: false,
        message: "New password must be at least 8 characters long",
      });
    }

    // Hash the new password
    const salt = await bcrypt.genSalt(10);
    const hashedPassword = await bcrypt.hash(newPassword, salt);

    // Update the user's password
    await userModel.findByIdAndUpdate(user._id, { password: hashedPassword });

    return res.status(200).send({
      success: true,
      message: "Password Reset Successfully",
    });
  } catch (error) {
    console.error("Error in forgot password controller:", error);
    return res.status(500).send({
      success: false,
      message: "Something went wrong",
      error: error.message, // Optionally include error message for debugging
    });
  }
};

